class MinStack:

    def __init__(self):
        """
        initialize your data structure here.
        """
        self.stack = []
        self.min_stack = []

    def push(self, x: int) -> None:
        self.stack.append(x)
        if self.min_stack and self.min_stack[-1] >= x:
            self.min_stack.append(x)
        if len(self.min_stack) == 0:
            self.min_stack.append(x)

    def pop(self) -> None:
        last = self.stack.pop()
        if last == self.min_stack[-1]:
            self.min_stack.pop()

    def top(self) -> int:
        return self.stack[-1]
        

    def getMin(self) -> int:
        return self.min_stack[-1]


